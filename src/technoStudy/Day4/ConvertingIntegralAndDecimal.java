package technoStudy.Day4;

public class ConvertingIntegralAndDecimal {
    public static void main(String[] args) {
        int intNumber = 1216465;
        float floatNumber = intNumber;
        System.out.println(floatNumber);

        float floatNumber2 = 1216465.61456f;
        int intNumber2 = (int) floatNumber2;
        System.out.println(intNumber2);
    }
}
