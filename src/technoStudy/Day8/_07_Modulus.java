package technoStudy.Day8;

public class _07_Modulus {
    public static void main(String[] args) {
        int x = 8; // dividend
        int y = 3; // divisor
        // when dividend is smaller than divisor then remainder is dividend itself

        System.out.println(x % y); // gives remainder of the division
        int result = x % y;
        System.out.println(result);
    }
}
