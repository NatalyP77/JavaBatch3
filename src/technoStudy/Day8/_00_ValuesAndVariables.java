package technoStudy.Day8;

public class _00_ValuesAndVariables {
    public static void main(String[] args) {
        String variable = "Hello!";

        System.out.println(variable); // printing the variable value

        variable = "Hi!";

        System.out.println(variable); // printing the variable value

        System.out.println("Hello!"); // printing the value itself
    }
}
