package technoStudy.Day13;

public class _01__ForLoop {
    public static void main(String[] args) {

        for(int counter = 0; counter < 5; counter++) {
            System.out.println(counter);
        }

        System.out.println();

        int counter2 = 0;
        while (counter2 < 5) {
            System.out.println(counter2);
            counter2++;
        }
    }
}
