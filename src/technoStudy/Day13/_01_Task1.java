package technoStudy.Day13;

public class _01_Task1 {
    // print numbers from 0 to 20 using for-loop
    // output should be in one line: 0123....20
    public static void main(String[] args) {
        for (int num = 0; num < 21; num = num + 1) {
            System.out.print(num);
        }
    }
}
