package technoStudy.Day3;

public class ByteDataType {
    public static void main(String[] args) {
        byte myFirstByte = 3;
        byte secondByte = 100;
        System.out.println(myFirstByte);
        System.out.println(secondByte);

        byte maxValue = 127;
        System.out.println(maxValue);

        byte minValue = -128;
        System.out.println(minValue);
    }
}
